import java.util.*;


public class Main {
    public static void main(String[] args) {
        // 入力された値を読み込み
        Scanner sc = new Scanner(System.in);
        int number1 = sc.nextInt();
        int number2 = sc.nextInt();
        
        // 値を足して出力
        System.out.println(number1 + number2);
    }
}